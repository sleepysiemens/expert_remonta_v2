Правила загрузки изображений для слайдов <br>

- загружайте в форматах jpg, png (лучше jpg) <br>
- изображения должны быть в ширину 800 пикселей и более <br>
- не загружайте изображения весом более 2 МБ, они не загрузятся, оптимизируйте перед загрузкой через любой сервис, например 
    <a href="https://imageresizer.com/image-compressor" target="_blank">imagecompressor</a>
